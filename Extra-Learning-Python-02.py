# strip

Name = " Alex "
# as you see there is a space between "" and Alex :

print(Name.strip())
# strip.()  output = 'Alex' (The space deleted)

print(Name.rstrip())
# rstrip.() r=right , so output = ' Alex'

print(Name.lstrip())
# lstrip.() l=left , so output = 'Alex '
# It is just used when you have written a string with space with "" 


