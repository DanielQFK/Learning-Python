# Strings
# what is string ? anything you put in "" or ''
# e.g. "Hello world" , 'Hello world'

# How we use:


name = "daniel"
print(name.title())
# .title() = It makes the first letter capital
print(name.upper())
# .upper() = It makes all letters capital
print(name.lower())
# .lower() = It makes all letters small

# the way you can use .title() , .upper() , .lower() is to put them after variable name
